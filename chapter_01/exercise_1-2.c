/**
 *
 * The program prints a character string that contains \c,
 * as an argument of the printf function.
 *
 */

#include <stdio.h> // Standard I/O library for printf.

int main() // The main function - the entry point of the program.
{
    // Call the printf function to print the character string argument.
    printf("Hello\c, world\n");

    // Return 0 to indicate successful program termination.
    return 0;

}
